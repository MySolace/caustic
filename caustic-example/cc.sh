#!/bin/bash
####################################################################################################
#                                          Build Compiler                                          #
####################################################################################################
cd ..
./pants binary -q caustic-compiler/src/main/scala:bin
mv dist/caustic-compiler.jar caustic-example/
cd caustic-example

####################################################################################################
#                                           Run Compiler                                           #
####################################################################################################
java -jar ./caustic-compiler.jar $1 $2
rm ./caustic-compiler.jar

